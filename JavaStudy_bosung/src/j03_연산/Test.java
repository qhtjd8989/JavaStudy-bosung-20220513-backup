package j03_연산;

public class Test {
	
	public static void main(String[] args) {
		int num1 = 5;
		int num2 = 2;
		/*
			+ : 더하기
			- : 빼기
			* : 곱하기
			/ : 나누기
		 	$ : 나머지
		*/
		
		System.out.println(num1 + num2);
		System.out.println(num1 - num2);
		System.out.println(num1 * num2);
		System.out.println(num1 / num2);
		System.out.println(num1 % num2);
		
	}
}
