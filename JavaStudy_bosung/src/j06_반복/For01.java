package j06_반복;

public class For01 {
	
	public static void main(String[] args) {
		
//		for문은 규칙적인 반복을 할 때 사용
		for(int i = 0; i < 10; i++) { // for문 실행순서  for(1번; 2번; 4번) {3번}
			System.out.println(i + 1);
		}
//		while문은 자유로운 반복을 할 때 사용
//		while(true) {
//			System.out.println();
//		}
		
		for(int i = 0; i < 10; i++) {
			System.out.println(10 - 1);
		}
	}
}
